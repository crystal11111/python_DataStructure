class Calc:
    def plus(self, n1, n2):
        print(n1 + n2)

    def minus(self, n1, n2):
        print(n1 - n2)

    def multi(self, n1, n2):
        print(n1 * n2)

    def divide(self, n1, n2):
        print(n1 // n2)

casio = Calc()
casio.plus(7, 7)
casio.minus(8,4)
casio.multi(3, 8)
casio.divide(9,3)